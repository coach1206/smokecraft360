def process_file(path: str) -> bool:
    with open(path, "r", encoding="utf-8", errors="replace") as f:
        original = f.read()
    content = original
    for old, new in REPLACEMENTS:
        content = content.replace(old, new)
    if content != original:
        with open(path, "w", encoding="utf-8") as f:
            f.write(content)
        return True
    return False


def main():
    changed, skipped = [], []
    for root, dirs, files in os.walk(SRC):
        # Skip node_modules, dist, .cache if they ended up inside src
        dirs[:] = [d for d in dirs if d not in ("node_modules", "dist", ".cache")]
        for filename in files:
            if not filename.endswith((".tsx", ".ts")):
                continue
            path = os.path.join(root, filename)
            try:
                if process_file(path):
                    changed.append(path)
                else:
                    skipped.append(path)
            except Exception as e:
                print(f"  ERROR {path}: {e}", file=sys.stderr)

    print(f"Retheme complete: {len(changed)} files modified, {len(skipped)} unchanged.")
    if changed:
        for p in sorted(changed):
            print(f"  ✓ {p.replace(SRC + '/', '')}")


if __name__ == "__main__":
    main()
